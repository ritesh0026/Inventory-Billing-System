    private void duplicate_bill_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_duplicate_bill_btnActionPerformed
        String dup_order_id = JOptionPane.showInputDialog("Enter the Order Id");
        try(Connection con = new DBConnection().getConnection()){
            Statement st = con.createStatement();
            ResultSet rs=st.executeQuery("SELECT * FROM orders WHERE order_id='"+dup_order_id+"'");
            if(rs.next()){
                Invoice dup_invoice=new Invoice(dup_order_id);
                dup_invoice.setVisible(true);
                
            }else
                JOptionPane.showMessageDialog(null, "No Such Order exists!! Please try again with a correct Order ID !! ", "Alert", JOptionPane.ERROR_MESSAGE);
        }catch(Exception e){
            System.err.println(e);
        }
    }//GEN-LAST:event_duplicate_bill_btnActionPerformed

