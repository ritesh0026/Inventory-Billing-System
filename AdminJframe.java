    private void check_order_details_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_check_order_details_btnActionPerformed

        String order_id = JOptionPane.showInputDialog("Enter the Order Id :");
            if(order_id!=null){
                try(Connection con = new DBConnection().getConnection()){
                    Statement st = con.createStatement();
                    ResultSet rs = st.executeQuery("Select * from orders Where order_id='"+order_id+"'");
                    if(rs.next()){
                        OrderDetails show_od_details = new OrderDetails();
                        show_od_details.show_order_details(order_id);
                        show_od_details.setVisible(true);
                    }else{
                        JOptionPane.showMessageDialog(null,"No such Order EXIST, Please Try again with a valid Order ID","ERROR",JOptionPane.ERROR_MESSAGE);
                }
                }catch(Exception e){
                    System.err.println(e);
                }
            }
    }//GEN-LAST:event_check_order_details_btnActionPerformed

